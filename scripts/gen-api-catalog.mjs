#!/usr/bin/env node
// Regenerates docs/api/catalog.json — the machine-readable index of every REST
// endpoint, derived straight from app/api/**/route.ts so it never drifts.
//
// Run it whenever you add/remove/rename an API route:
//   node scripts/gen-api-catalog.mjs
//
// The MCP server (mcp/server.mjs) serves this file via `mediabox_list_endpoints`
// so the assistant can discover endpoints without reading the prose docs.

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, "..");
const API_DIR = path.join(ROOT, "app", "api");
const OUT = path.join(ROOT, "docs", "api", "catalog.json");

// Routes reachable without authentication (mirrors proxy.ts PUBLIC_API).
const PUBLIC = new Set([
  "/api/v1/health",
  "/api/v1/auth/login",
  "/api/v1/auth/setup",
  "/api/v1/auth/kiosk",
]);

/** Recursively collect every route.ts under app/api. */
function findRoutes(dir) {
  const out = [];
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) out.push(...findRoutes(full));
    else if (entry.name === "route.ts") out.push(full);
  }
  return out;
}

/** app/api/v1/movies/[id]/route.ts -> /api/v1/movies/{id} */
function toApiPath(file) {
  const rel = path.relative(ROOT, file).replace(/\\/g, "/");
  return "/" + rel.replace(/^app\//, "").replace(/\/route\.ts$/, "").replace(/\[([^\]]+)\]/g, "{$1}");
}

function detectMethods(src) {
  const methods = [];
  const re = /export\s+(?:async\s+)?function\s+(GET|POST|PUT|PATCH|DELETE|HEAD)\b/g;
  let m;
  while ((m = re.exec(src))) if (!methods.includes(m[1])) methods.push(m[1]);
  return methods;
}

function detectAuth(src, apiPath) {
  if (/requireAdmin/.test(src)) return "admin";
  if (/requireUser/.test(src)) return "user";
  if (PUBLIC.has(apiPath)) return "public";
  return "session"; // any authenticated session or x-api-key
}

/** Best-effort one-line summary: first line of the first JSDoc/// comment. */
function detectSummary(src) {
  const jsdoc = src.match(/\/\*\*\s*\n?\s*\*?\s*(.+)/);
  if (jsdoc) return jsdoc[1].replace(/\*\/\s*$/, "").trim();
  const line = src.match(/^\s*\/\/\s*(.+)$/m);
  if (line) return line[1].trim();
  return "";
}

const routes = findRoutes(API_DIR).sort();
const endpoints = [];
for (const file of routes) {
  const src = fs.readFileSync(file, "utf8");
  const apiPath = toApiPath(file);
  const methods = detectMethods(src);
  if (methods.length === 0) continue;
  const auth = detectAuth(src, apiPath);
  const summary = detectSummary(src);
  for (const method of methods) endpoints.push({ method, path: apiPath, auth, summary });
}

endpoints.sort((a, b) => a.path.localeCompare(b.path) || a.method.localeCompare(b.method));

const catalog = {
  generatedFrom: "app/api/**/route.ts",
  note: "Auto-generated by scripts/gen-api-catalog.mjs — do not edit by hand. Re-run after changing routes.",
  count: endpoints.length,
  endpoints,
};

fs.mkdirSync(path.dirname(OUT), { recursive: true });
fs.writeFileSync(OUT, JSON.stringify(catalog, null, 2) + "\n");
console.error(`Wrote ${endpoints.length} endpoints to ${path.relative(ROOT, OUT)}`);
